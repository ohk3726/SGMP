package model;

public class Sale_model {
	private String sale_id;
	private String prod_id;
	private String cust_id;
	private String sale_cnt;
	private String sale_date;
	private String sale_payment;
	public String getSale_id() {
		return sale_id;
	}
	public void setSale_id(String sale_id) {
		this.sale_id = sale_id;
	}
	public String getProd_id() {
		return prod_id;
	}
	public void setProd_id(String prod_id) {
		this.prod_id = prod_id;
	}
	public String getCust_id() {
		return cust_id;
	}
	public void setCust_id(String cust_id) {
		this.cust_id = cust_id;
	}
	public String getSale_cnt() {
		return sale_cnt;
	}
	public void setSale_cnt(String sale_cnt) {
		this.sale_cnt = sale_cnt;
	}
	public String getSale_date() {
		return sale_date;
	}
	public void setSale_date(String sale_date) {
		this.sale_date = sale_date;
	}
	public String getSale_payment() {
		return sale_payment;
	}
	public void setSale_payment(String sale_payment) {
		this.sale_payment = sale_payment;
	}
	
	
}
